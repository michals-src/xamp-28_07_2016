<field class="sub-field" id="defaults.enedSetColor">
	<p class="lead">enedSetColor</p>
	<p>Shows or hides ened trigger.</p>
	<p>if is <code>true</code> then trigger is visible</p>
<pre>
<code class="JavaScript">
enedscroll.option({
	'enedSetColor': true, //The trigger is visible.
});

</code>
</pre>
<p>if is <code>false</code> then trigger is hidden</p>
<pre>
<code class="JavaScript">
enedscroll.option({
	'enedSetColor': false, //The trigger is hidden.
});

</code>
</pre>

</field>