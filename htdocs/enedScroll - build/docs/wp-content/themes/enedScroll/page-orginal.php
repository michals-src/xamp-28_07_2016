<h2 class="title">Trigger</h2>
<p>This is a magic wand with which you can determine when to call your scene. For a more detailed description of Trigger you will find it here {{ a > trigger - add element }}.</p>
</article>
<article>
<h2 class="title">Selectors</h2>
<p>Selectors as trigger can determine moment to call your event. Exists 2 selectors, top and bottom. Specifies a moment of a function call is based on the element that was assigned to the scene as a target.</p>
</article>
<article>
<h2 class="title">Scene</h2>
<p>It is basic space to create animations. All parameters like duration are connected with scene parameters.</p>
</article>
