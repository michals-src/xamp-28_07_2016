<pre><code class="language-javascript">scene.add( @element, @transitions, @delay );</code></pre>
						<div class="specs">
							<h1 class="title">Specification</h1>
							<div class="columns column">
								<div class="column-medium-3"><h2>Element</h2></div>
								<div class="column-medium-5">
									<header>
										<div class="row"><p class="describe">It is item which we want to manipulate.</p></div>
									</header>
									<div class="row">
									Type <i class="lighting inline">object</i>
<p class="describe"><code class="language-markup">$('.item')</code></p>
									</div>
								</div>
							</div>							
							<div class="columns column">
								<div class="column-medium-8"><h2>Transitions</h2></div>
								<div class="column-medium-8">
									<header>
										<div class="row"><p class="describe">It is set of css values which you want to change with scrolling.</p></div>
									</header>
									<div class="row">
<p class="describe"><pre><code class="language-javascript">[
	[ { css_property: the_css_property_value, ... }, duration, offset ],
	...
]</code></pre></p>
								</div>
								<div class="row">
									<div class="columns">
										<div class="column-medium-3"><h3>css_property</h3></div>
										<div class="column-medium-5" style="margin-bottom: 20px;">
											<header>
												<p class="describe">It is css attribute e.g. opacity, transform. Color supports only rgb, rgba</p>
											</header>
											<div class="row">Type <i class="lighting">string</i></div>
										</div>
										<div class="column-medium-3"><h3>the_css_property_value</h3></div>
										<div class="column-medium-5" style="margin-bottom: 20px;">
											<header>
												<p class="describe">It is css attribute value. Takes from to value.</p>
											</header>
											<div class="row">Type <i class="lighting">string</i></div>
										</div>
										<div class="column-medium-3"><h3>Duration</h3></div>
										<div class="column-medium-5" style="margin-bottom: 20px;">
											<header>
												<p class="describe">Duration of transition.</p>
												<p>Integer 0.1 = 100</p>
												<p>String 100 = 100</p>
												<p class="describe">String 50% = 50% of scene</p>
											</header>
											<div class="row">Type <i class="lighting">integer</i><i class="lighting">string</i></div>
										</div>
										<div class="column-medium-3"><h3>Offset</h3></div>
										<div class="column-medium-5" style="margin-bottom: 20px;">
											<header>
												<p class="describe">Offset of transition. The offset is added to previous transition offset.</p>
												<p>Integer 0.1 = 100</p>
												<p>String 100 = 100</p>
												<p class="describe">String 50% = 50% of scene</p>
											</header>
											<div class="row">Type <i class="lighting">integer</i><i class="lighting">string</i></div>
										</div>
									</div>
									</div>
								</div>
							</div>
