						<h2>Overall</h2>
						<p>It is kernel of creating animations and points to call events. The function connecting all values, target as scene, trigger, selectors and visual. You can use the function by following the example below.</p>
<pre><code class="language-javascript">var scene = new enedScroll( @target [, @trigger [, @options ] ] ); </code></pre>
